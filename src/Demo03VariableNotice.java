public class Demo03VariableNotice {

    public static void main (String[] args) {
        float num1 = 3.14F;
        System.out.println(num1);
        float num2 = 3.14F;
        System.out.println(num2);
        long num3 = 33333;
        long num4 = 333333L;
        System.out.println(num3);
        System.out.println(num4);
    } 
}